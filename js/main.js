/* start Table Slider*/
function myFunction() {
    "use strict";
    var change = document.getElementById("btn-slideT");
    if (change.innerHTML === "Hide Nutrition Information") {
        change.innerHTML = "Show Nutrition Information";
    } else {
        change.innerHTML = "Hide Nutrition Information";
    }
}



$("document").ready(
    function() {
        "use strict";
        $("#btn-slideT").click(
            function() {
                $("#slide-table").slideToggle("slow");

            });
    });

$(document).ready(function() {
    "use strict";
    $('#slide-table').hide();
});

